package org.patientdetails.controller.builder;

import org.nhs.patient.resource.beans.PatientDetailsReq;
import org.patientdetails.controller.bean.RequestBean;

public class ReqBuilder {

	public PatientDetailsReq buildPatientDetailsReq(RequestBean req) {
		PatientDetailsReq request = new PatientDetailsReq();
		request.setCreatedBy(req.getCreatedBy());
		request.setCreatedDate(req.getCreatedDate());
		request.setDOB(req.getDateofBirth());
		request.setFname(req.getFirstName());
		request.setGender(req.getGender());
		request.setHnum(req.getHnum());
		request.setId(req.getId());
		request.setLname(req.getLastName());
		request.setNationalIdentifierType(req.getNationalIdentifierType());
		request.setNationalPatientId(req.getNationalPatientId());
		request.setNhsNumber(req.getNhsNumber());
		request.setPostalCode(req.getPostalCode());
		request.setUpdatedBy(req.getUpdatedBy());
		request.setUpdatedDate(req.getUpdatedDate());
		return request;

	}

}
